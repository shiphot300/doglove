한국 역사의 시대 구분은 구석기 시대에서부터 신석기 시대, 청동기 시대, 철기 시대로 구분되는 선사시대와 고조선의 성립 이후 원삼국 시대, 삼국 시대, 남북국 시대, 후삼국 시대까지의 고대, 고려 시대인 중세, 조선 시대인 근세, 대한제국 수립 이후 오늘날까지의 근·현대 등으로 구분된다.
한국의 역사는 고찰하고자 하는 영역에 따라 한국의 경제사 , 한국의 생활사, 한국의 정치사와 같이 분야별 역사로 나뉘어 다루어지기도 한다.
